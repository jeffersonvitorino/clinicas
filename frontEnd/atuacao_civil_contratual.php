        <h3>Civil e Contratual</h3>
        
        <p><b>Civil</b><br />Atuação no direito de posse e propriedade, Direito das Obrigações e Responsabilidade Civil na forma consultiva e contenciosa, no estudo e na elaboração de peças, formulação de defesas, interposição de recursos e acompanhamento de processos frente ao poder judiciário e a administração pública.</p>
        
        <p><b>Contratual</b><br />Assessoria jurídica para pessoas físicas e jurídicas (empresas nacionais e estrangeiras), na elaboração e negociação de contratos e na defesa dos interesses do cliente em contendas judiciais originárias dos mesmos.</p>
    
