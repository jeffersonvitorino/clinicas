<h2>Procedimentos adotados pelo FNECDC para novas filiações</h2>

<p>Para se tornar membro do Fórum Nacional das Entidades Civis de Defesa do Consumidor – FNECDC – os interessados devem antes de qualquer coisa conhecer o estatuto e princípios éticos do FNECDC. Os princípios éticos do FNECDC são regidos por aspectos como independência, transparência, democracia, solidariedade e compromisso social (confira neste Site no link "Princípios Éticos").</p>

<p>As entidades que pretendem se filiar ao FNECDC precisam apresentar uma declaração de cumprimento dos princípios éticos do FNECDC; cópia autenticada do estatuto da entidade devidamente registrado em cartório; atas da eleição e posse da atual diretoria; plano de trabalho anual das entidades; relatório financeiro do último exercício; certidão do cartório do registro do estatuto, exemplares de suas publicações (jornais, boletins e folders). Como parte complementar: apresentar duas cartas de apresentação, sendo uma de instituição pública e uma de entidade civil.</p>

<p>Toda documentação deve ser enviada para a Secretaria Executiva do FNECDC, aos cuidados de Geraldo Guerra Junior, na Rua do Riachuelo, 105, sala 219 – Boa Vista, Cep 50050-400 – Recife – PE.</p>

