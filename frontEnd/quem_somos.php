<h2>Recursos Financiamento</h2>

<p>O Fórum Nacional de Entidades Civis de Defesa do Consumidor é mantido pelas entidades-membro, especialmente as integrantes do Conselho Diretor, podendo, ainda, obter recursos através de projetos e convênios com instituições nacionais e estrangeiras.</p>

<p>A principal apoiadora do Fórum atualmente é a Fundação Ford, uma organização privada, sem fins lucrativos, criada nos Estados Unidos em 1936 para ser uma fonte de apoio do terceiro setor em todo o mundo.</p>

<p>Atualmente, não possui ações da Companhia Ford e sua diversificada carteira de investimentos é administrada para ser uma fonte permanente de recursos para custear seus programas, apoiando pessoas e organizações comprometidas com a consolidação da democracia, a redução da pobreza e da injustiça social e com o desenvolvimento humano.</p>

<p>O Escritório do Brasil, localizado na cidade do Rio de Janeiro, está entre os mais antigos dos doze escritórios que a Fundação Ford mantém no exterior. Ana Toni é a representante do Escritório do Brasil da Fundação Ford.</p>