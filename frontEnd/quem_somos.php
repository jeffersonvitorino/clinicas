<h1>Sobre o FNECDC</h1>

<p>O Fórum Nacional das Entidades Civis de Defesa do Consumidor – FNECDC – é uma instância de articulação das organizações sociais que atuam na defesa do consumidor no Brasil. Foi criado para fortalecer o movimento dos consumidores no País e não para ser apenas mais uma entidade. Busca ser um efetivo espaço de articulação, troca de informações, apoio e ação conjunta, dentro de uma perspectiva de manutenção da pluralidade do movimento.</p>

<h2>Objetivos</h2>

<p>O objetivo do Fórum Nacional das Entidades Civis de Defesa do Consumidor é fortalecer o movimento de defesa do consumidor no Brasil, promovendo a articulação das entidades civis do setor.</p>

<h2>Identidade</h2>

<p>O Fórum Nacional das Entidades Civis de Defesa do Consumidor tem como identidade política e institucional a defesa dos interesses dos consumidores, sendo entidade civil, de caráter nacional, formadas pelas entidades civis que desenvolvem suas atividades de acordo com princípios éticos comuns.</p>

<h2>Estrutura Organizacional</h2>

<p>O Fórum Nacional das Entidades Civis de Defesa do Consumidor é administrado por um Conselho Diretor, composto por um presidente e quatro conselheiros, um Conselho Fiscal, constituído por três membros, todos eleitos em Assembléia Geral (instância soberana de decisão da entidade) para um mandato de quatro anos. Os conselheiros eleitos por ocasião da Assembléia de instituição do FNECDC são os seguintes:</p>

