<h1>Princípios Éticos</h1>

<p>Estes princípios se aplicam às entidades civis que atuem, de forma exclusiva ou não, em defesa do consumidor no território nacional, participantes do Fórum Nacional das Entidades Civis de Defesa do Consumidor. Entidade Civil de Defesa do Consumidor é uma pessoa jurídica de direito privado, com finalidade social, sem fins lucrativos, independente, que tenha como missão a defesa dos interesses dos consumidores, pessoas físicas. Os princípios se aplicam às entidades de caráter local, regional ou nacional.</p>

<h2>Princípio da Independência</h2>

<p>As entidades civis de defesa do consumidor devem ser independentes de empresas, governos, partidos políticos, não podendo inclusive receber qualquer tipo de fundos ou doações que possam comprometer sua independência de agir ou emitir opiniões.</p>

<h2>Princípio da Transparência e Democracia</h2>

<p>Os estatutos da entidade devem conter mecanismos que assegurem a democracia e a transparência nas diferentes instâncias da entidade. Devem garantir que seu patrimônio e receita sejam compostos por recursos provenientes de atividades pertinentes e legítimas à natureza e aos fins institucionais da Entidade. As atividades das entidades civis de defesa do consumidor devem assegurar a transparência na gestão da entidade, na relação com os seus associados e com a sociedade.</p>

<p>Os bens e recursos das entidades de defesa do consumidor devem ser usados exclusivamente para a realização de seus objetivos.</p>

<p>Em caso de extinção, o patrimônio da entidade de defesa do consumidor deverá necessariamente ser destinado a entidades com finalidades semelhantes e sem fins lucrativos.</p>

<h2>Princípio da Solidariedade</h2>

<p>As entidades civis de defesa do consumidor devem estabelecer, sempre que possível, mecanismos de troca de informação, de ajuda mútua que possibilitem a articulação entre elas, com ações conjuntas e de apoio às outras entidades, de forma a que o movimento de consumidores se fortaleça como um todo.</p>

<h2>Princípio do Compromisso Social</h2>

<p>As entidades civis de defesa do consumidor devem estar comprometidas com as questões sociais e com a cidadania, buscando sempre contribuir para melhorar a qualidade de vida da comunidade.</p>