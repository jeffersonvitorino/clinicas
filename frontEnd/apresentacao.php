        <h3>Apresentação</h3>
        
        <p>Costa, Campello & Medeiros Advogados Associados é uma sociedade de advogados que foi sedimentada por valores éticos e morais como princípios fundamentais, que vêm sendo transmitidos a novas gerações de profissionais.</p>

        <p>Com a experiência de seus sócios, adquirida pela atuação em departamento jurídico de grandes empresas, nacionais e multinacionais, foi possível desenvolver um modelo de escritório de negócios, cujo foco prioritário está em oferecer aos seus clientes apoio integral com soluções de qualidade técnica nas diversas áreas do direito ligadas à atividade empresarial. 
        O escritório é conhecido e respeitado por sua atuação em diversos ramos do direito, situando-se como referência na região Nordeste do Brasil e oferecendo um atendimento personalizado de advocacia preventiva, contenciosa nos diversos Tribunais, bem como, frente aos diversos entes administrativos. Presta assessoria jurídica e consultoria para pessoas físicas ou jurídicas, nacionais ou estrangeiras.</p>
        
        <p>Ao longo dos anos Costa, Campello & Medeiros Advogados Associados, com uma equipe de profissionais experientes e qualificados e aliança estratégica com escritórios correspondentes nas diversas regiões do país e no exterior, apoiada em estrutura administrativa e tecnológica, vem crescendo de forma sustentável, garantindo uma gestão profissional, dinâmica, moderna, em uma constante busca da excelência dos serviços prestados.</p>
        
        <p>O escritório dispõe de uma moderna infraestrutura, biblioteca atualizada, e sistema informatizado de gerenciamento de processos, além de investir regularmente para capacitação e especialização contínua de seus profissionais.</p>
        
